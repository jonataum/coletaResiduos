CREATE SEQUENCE SEQ_USUARIOS
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;

CREATE SEQUENCE SEQ_COLETA
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;

CREATE SEQUENCE SEQ_PONTO_DESCARTE
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;


CREATE TABLE TBL_USUARIOS (
    USUARIO_ID INTEGER DEFAULT SEQ_USUARIOS.NEXTVAL PRIMARY KEY,
    NOME VARCHAR2(100) NOT NULL,
    EMAIL VARCHAR2(100) UNIQUE NOT NULL,
    SENHA VARCHAR2(60) NOT NULL,
    ROLE VARCHAR(50) DEFAULT 'USER' NOT NULL
);

CREATE TABLE TBL_PONTO_DESCARTE (
    PONTO_ID INTEGER DEFAULT SEQ_PONTO_DESCARTE.NEXTVAL PRIMARY KEY,
    PONTO_NOME VARCHAR2(150) NOT NULL,
    PONTO_ENDERECO VARCHAR2(200) UNIQUE NOT NULL,
    PONTO_CAPACIDADE_MAX NUMBER,
    PONTO_QUANT_ATUAL NUMBER,
    TIPO_RESIDUO VARCHAR2(200) NOT NULL
);

CREATE TABLE TBL_COLETA (
    COLETA_ID INTEGER DEFAULT SEQ_COLETA.NEXTVAL PRIMARY KEY,
    COLETA_DATA DATE NOT NULL,
    PONTO_ID INTEGER NOT NULL,
    CONSTRAINT fk_ponto_coleta FOREIGN KEY (ponto_id) REFERENCES tbl_ponto_descarte (ponto_id)
);
